int	ft_str_is_lowercase(char *str)
{
	int i;
	int e;

	e = 1;
	i = 0;
	while (*(str + i) != '\0')
	{
		if (*(str + i) < 97 || *(str + i) > 122)
		{
			e = 0;
			return (e);
		}
		i++;
	}
	return (e);
}
