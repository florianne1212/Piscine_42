int	ft_strcmp(char *s1, char *s2)
{
	int i;
	int e;

	i = 0;
	e = 0;
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] < s2[i] || s1[i] > s2[i])
		{
			e = s1[i] - s2[i];
			return (e);
		}
		else if (s1[i] == s2[i])
		{
			i++;
		}
	}
	e = s1[i] - s2[i];
	return (e);
}
