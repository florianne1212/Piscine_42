int					ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				e;

	i = 0;
	e = 0;
	if (n == 0)
	{
		return (0);
	}
	while (s1[i] != '\0' && s2[i] != '\0' && i < n)
	{
		if (s1[i] != s2[i])
		{
			e = s1[i] - s2[i];
			return (e);
		}
		i++;
	}
	if (i < n)
	{
		e = s1[i] - s2[i];
		return (e);
	}
	return (0);
}
