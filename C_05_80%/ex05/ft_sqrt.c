int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb == 1)
	{
		return (1);
	}
	while ((i < nb) && (i < 46341))
	{
		if (nb == (i * i))
		{
			return (i);
		}
		i++;
	}
	return (0);
}
